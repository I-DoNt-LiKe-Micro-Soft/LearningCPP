/*Implicit and explicit conversion
C++ allows us to convert data of one type to that of another. This is known as type conversion.
There are two types of type conversion in C++, Implicit Conversion and Explicit Conversion (also known as Type Casting)
*/

/*Implicit conversion
The type conversion that is done automatically done by the compiler is known as implicit type conversion,
this type of conversion is also known as automatic conversion.
*/

/*Explicit conversion
When the user manually changes data from one type to another, this is known as explicit conversion. This type of conversion is also known as type casting.
There are three major ways in which we can use explicit conversion in C++. They are C-style type casting (also known as cast notation), 
function notation (also known as old C++ style type casting) and type conversion operators.

C-style type casting, as the name suggests this type of casting is favored by the C programming language, It is also known as cast notation, The syntax for this style is:
(data_type)expression;

Function-style Casting, we can also use the function like notation to cast data from one type to another, the syntax for this style is:
data_type(expression);

Type Conversion Operators, besides these two type castings, C++ also has four operators for type conversion. They are known as type conversion operators. 
They are static_cast, dynamic_cast, const_cast and reinterpret_cast.
*/

